/**
 * E2E.2 — single source of truth for platform feature flag contract metadata.
 *
 * Every key in PLATFORM_FEATURE_DEFINITIONS must appear here.
 * Register new platform flags here when adding a Global platform toggle.
 * UI labels must match platform-feature-definitions.ts exactly.
 */

export type PlatformFeatureOwnership = 'database' | 'environment'

export type PlatformFeatureCategory =
  | 'accessibility'
  | 'admin'
  | 'ai'
  | 'analytics'
  | 'boards'
  | 'commerce'
  | 'grading'
  | 'identity'
  | 'integration'
  | 'learner'
  | 'platform'
  | 'quizzes'
  | 'transcripts'

export type PlatformFeatureMatrixEntry = {
  key: string
  /** Settings row label — must match PLATFORM_FEATURE_DEFINITIONS. */
  label: string
  category: PlatformFeatureCategory
  /**
   * database — mutable via PUT /settings/platform (Global platform toggles).
   * environment — process-env owned; UI must be read-only (none of the boolean
   * definitions are environment-owned today; reserved for contract coverage).
   */
  ownershipSource: PlatformFeatureOwnership
  /** Maps into PlatformSettingsPayload.sources when the API reports field provenance. */
  sourceKey: string | null
  /** Key on GET /api/v1/platform/features, or null when settings-only. */
  runtimeKey: string | null
  /** Required when runtimeKey is null — documents intentional settings-only flags. */
  settingsOnlyRationale: string | null
  /** Never log or round-trip secret values for this key (booleans are false). */
  secretSensitive: boolean
  /** Include in the representative Global platform UI sample suite. */
  uiSample: boolean
  /** Optional gated surface hint for operators (not asserted by the contract suite). */
  gatedSurface: string | null
}

