/**
 * Parse PLATFORM_FEATURE_DEFINITIONS keys from the web source (for parity tests).
 */
export function parsePlatformFeatureDefinitionKeys(source: string): string[] {
  const keys: string[] = []
  // Match definition entries only (`key: '…'`), not `sourceKey:`.
  const re = /^\s*key:\s*'([^']+)'/gm
  for (const match of source.matchAll(re)) {
    keys.push(match[1])
  }
  return keys
}

/** Validate matrix uniqueness and required metadata (unit-level). */
export function validatePlatformFeatureMatrix(definitionKeys?: readonly string[]): string[] {
  const errors: string[] = []
  const keys = new Set<string>()
  const labels = new Set<string>()

  for (const entry of PLATFORM_FEATURE_MATRIX) {
    if (keys.has(entry.key)) errors.push(`duplicate key: ${entry.key}`)
    keys.add(entry.key)

    if (labels.has(entry.label)) errors.push(`duplicate label: ${entry.label}`)
    labels.add(entry.label)

    if (!entry.label.trim()) errors.push(`${entry.key} missing label`)
    if (!entry.category) errors.push(`${entry.key} missing category`)

    if (entry.ownershipSource !== 'database' && entry.ownershipSource !== 'environment') {
      errors.push(`${entry.key} invalid ownershipSource`)
    }

    if (entry.runtimeKey == null) {
      if (!entry.settingsOnlyRationale?.trim()) {
        errors.push(`${entry.key} settings-only flag missing settingsOnlyRationale`)
      }
    } else if (entry.settingsOnlyRationale != null) {
      errors.push(`${entry.key} has runtimeKey but also settingsOnlyRationale`)
    }

    if (entry.secretSensitive) {
      errors.push(`${entry.key}: boolean matrix entries must not be secretSensitive`)
    }
  }

  if (definitionKeys) {
    const defSet = new Set(definitionKeys)
    for (const key of definitionKeys) {
      if (!keys.has(key)) {
        errors.push(`definition key missing from manifest: ${key}`)
      }
    }
    for (const key of keys) {
      if (!defSet.has(key)) {
        errors.push(`manifest key not in PLATFORM_FEATURE_DEFINITIONS: ${key}`)
      }
    }
    if (definitionKeys.length !== PLATFORM_FEATURE_MATRIX.length) {
      errors.push(
        `definition/manifest count mismatch: defs=${definitionKeys.length} manifest=${PLATFORM_FEATURE_MATRIX.length}`,
      )
    }
  }

  // Exactly one UI sample per category
  for (const cat of PLATFORM_FEATURE_CATEGORIES) {
    const samples = PLATFORM_FEATURE_MATRIX.filter((e) => e.category === cat && e.uiSample)
    if (samples.length !== 1) {
      errors.push(`category ${cat} expected 1 uiSample, got ${samples.length}`)
    }
  }

  return errors
}

export function entriesForCategory(category: PlatformFeatureCategory) {
  return PLATFORM_FEATURE_MATRIX.filter((e) => e.category === category)
}

export function apiContractShards(shardCount = 3): PlatformFeatureMatrixEntry[][] {
  const owned = DATABASE_OWNED_PLATFORM_FEATURES
  const shards: PlatformFeatureMatrixEntry[][] = Array.from({ length: shardCount }, () => [])
  owned.forEach((entry, i) => {
    shards[i % shardCount]!.push(entry)
  })
  return shards
}
